    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        // TODO add your handling code here:
        
        String dinero=txt_monedas.getText();
    
        if(dinero.length()==0 ){
            JOptionPane.showMessageDialog(this,"Ingrese una cantidad");
        } else {
            Double money = Double.parseDouble(dinero);
            if(money>0){
            setMoneda(money);
            MenuMonedas mm =new MenuMonedas();
            mm.setVisible(true);
            dispose();
            }else{
            JOptionPane.showMessageDialog(this,"Ingrese una cantidad Positiva");
            }
        }        
    }//GEN-LAST:event_btn_okActionPerformed

